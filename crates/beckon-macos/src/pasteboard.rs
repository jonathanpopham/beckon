    #[test]
    fn marker_types_are_the_published_uti_set() {
        assert!(MARKER_TYPES.contains(&"org.nspasteboard.TransientType"));
        assert!(MARKER_TYPES.contains(&"org.nspasteboard.ConcealedType"));
        assert!(MARKER_TYPES.contains(&"org.nspasteboard.AutoGeneratedType"));
        assert_eq!(MARKER_TYPES.len(), 3);
    }

    #[test]
    fn should_store_rejects_marked_empty_and_oversized() {
        assert!(should_store("hello", false));
        // Privacy rule 1: a marker skips the change no matter the text.
        assert!(!should_store("hello", true));
        // Non-string or stringless changes read back as empty.
        assert!(!should_store("", false));
        // Privacy rule 2: the cap is inclusive; one byte over is out.
        let at_cap = "x".repeat(MAX_TEXT_BYTES);
        assert!(should_store(&at_cap, false));
        let over_cap = "x".repeat(MAX_TEXT_BYTES + 1);
        assert!(!should_store(&over_cap, false));
    }

    #[test]
    fn title_is_first_line_truncated_with_ellipsis() {
        assert_eq!(title_of("short"), "short");
        assert_eq!(title_of("line one\nline two"), "line one");
        assert_eq!(title_of(""), "");
        // Exactly at the budget: untouched.
        let exact = "e".repeat(TITLE_MAX_CHARS);
        assert_eq!(title_of(&exact), exact);
        // One over: 59 chars plus the ellipsis, 60 total.
        let over = "o".repeat(TITLE_MAX_CHARS + 1);
        let title = title_of(&over);
        assert_eq!(title.chars().count(), TITLE_MAX_CHARS);
        assert!(title.ends_with('…'));
        assert!(title.starts_with(&"o".repeat(TITLE_MAX_CHARS - 1)));
        // Truncation counts characters, not bytes.
        let unicode = "ü".repeat(TITLE_MAX_CHARS + 5);
        assert_eq!(title_of(&unicode).chars().count(), TITLE_MAX_CHARS);
    }

