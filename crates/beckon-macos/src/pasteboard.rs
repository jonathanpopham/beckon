//! NSPasteboard change watcher: the shell edge of clipboard history.
//!
//! An NSTimer polls the general pasteboard's changeCount every 0.5 s (the
//! pasteboard has no notification API; polling the counter is how every
//! clipboard manager does it). On a new count the plain-text contents are
//! read and recorded into a `beckon_core::clipstore::ClipStore`, which is
//! persisted to `<store_root>/clipboard.json` after every accepted entry.
//! The store file is written through `persist::write_atomic`, which
//! creates it with mode 0600, and the store root itself is 0700, so
//! clipboard history is owner-readable only.
//!
//! Privacy rules, non-negotiable:
//!
//! 1. A change whose type list carries any of the standard pasteboard
//!    privacy markers is skipped entirely: password managers and other
//!    secret-handling apps mark their writes with
//!    "org.nspasteboard.TransientType", "org.nspasteboard.ConcealedType",
//!    or "org.nspasteboard.AutoGeneratedType" (see nspasteboard.org).
//!    The check goes through [[pasteboard types] containsObject:].
//! 2. Strings larger than [`MAX_TEXT_BYTES`] (100 KB) are skipped: nobody
//!    scrolls a launcher panel for a megabyte of text, and the store file
//!    stays small.
//! 3. beckon's own pasteboard writes are never recorded. Every write path
//!    in this process must call [`note_own_write`] immediately after
//!    writing (activate() here does; the engine's calc-copy path does via
//!    the integrator), which tells the watcher to ignore that changeCount.
//!
//! Threading invariants: like the rest of the shell, everything here is
//! main-thread only in the running app. [`start`] attaches its timer to
//! the calling thread's run loop; the engine calls it on the main thread
//! during init, so the timer lives on the main run loop (in the common
//! modes, so it keeps ticking while the panel is up). The WATCHER Mutex
//! exists to satisfy Rust's static-safety rules, not to enable
//! cross-thread use, and is never held across an FFI call.

