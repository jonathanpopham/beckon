use crate::ffi::{self, msg, Bool, Id, Sel};
use beckon_core::clipstore::{ClipEntry, ClipStore};
use beckon_core::persist;
use beckon_core::router::{Item, ItemKind};
use std::mem::transmute;
use std::path::{Path, PathBuf};
use std::sync::{Mutex, OnceLock};
use std::time::{SystemTime, UNIX_EPOCH};

/// Poll interval for the changeCount timer, in seconds. Half a second is
/// the customary clipboard-manager cadence: imperceptible to the user,
/// negligible cost (one integer read per tick when nothing changed).
const POLL_INTERVAL_SECONDS: f64 = 0.5;

/// Privacy rule 2: skip strings larger than this many bytes (100 KB).
const MAX_TEXT_BYTES: usize = 100 * 1024;

/// Privacy rule 1: pasteboard marker types that mean "do not record".
/// These are the de facto standard published at nspasteboard.org and
/// honored by password managers.
const MARKER_TYPES: [&str; 3] = [
    "org.nspasteboard.TransientType",
    "org.nspasteboard.ConcealedType",
    "org.nspasteboard.AutoGeneratedType",
];

/// The pasteboard type read and written here (the literal value of
/// NSPasteboardTypeString; no headers are linked to import the constant).
const PASTEBOARD_TYPE_STRING: &str = "public.utf8-plain-text";

/// The clipboard history file, under the beckon store root.
const CLIPBOARD_FILE: &str = "clipboard.json";

/// Rows [`items`] returns at most: the same single-keystroke panel depth
/// the engine uses for app results.
const MAX_ITEMS: usize = 9;

/// Title budget in characters, ellipsis included.
const TITLE_MAX_CHARS: usize = 60;

struct Watcher {
    store: ClipStore,
    /// Where the store persists; saved after every accepted entry.
    path: PathBuf,
    /// The changeCount already handled (or deliberately ignored). A tick
    /// that reads this exact value does nothing.
    last_change_count: isize,
}

static WATCHER: OnceLock<Mutex<Watcher>> = OnceLock::new();

fn watcher() -> Option<&'static Mutex<Watcher>> {
    WATCHER.get()
}

/// Current wall clock as unix seconds; a clock before the epoch degrades
/// to 0 rather than panicking. (Same policy as the engine's clock read.)
fn now_secs() -> u64 {
    SystemTime::now()
        .duration_since(UNIX_EPOCH)
        .map(|d| d.as_secs())
        .unwrap_or(0)
}

/// Load the persisted history. Missing file is a fresh install; a corrupt
/// file must not take the launcher down, so it logs to stderr and starts
/// fresh (the next save overwrites it).
fn load_store(path: &Path) -> ClipStore {
    match ClipStore::load(path) {
        Ok(Some(store)) => store,
        Ok(None) => ClipStore::new(),
        Err(e) => {
            eprintln!(
                "beckon: corrupt clipboard store {}: {e}; starting fresh",
                path.display()
            );
            ClipStore::new()
        }
    }
}

/// Begin watching the general pasteboard. Idempotent: the second and
/// later calls return immediately. Loads the persisted store, snapshots
/// the current changeCount (whatever is on the pasteboard at startup is
/// not captured; only changes from here on are), and schedules the poll
/// timer on the calling thread's run loop. The engine calls this once on
/// the main thread, so in the app the timer lives on the main run loop.
pub fn start() {
    if WATCHER.get().is_some() {
        return;
    }
    let root = match persist::ensure_store_root() {
        Ok(root) => root,
        Err(e) => {
            let fallback = persist::store_root();
            eprintln!(
                "beckon: cannot create store root {}: {e}; clipboard history may not persist",
                fallback.display()
            );
            fallback
        }
    };
    let path = root.join(CLIPBOARD_FILE);
    let store = load_store(&path);
    // Safety: changeCount has encoding q@: (returns NSInteger, no args).
    let last_change_count = unsafe {
        let pb = general_pasteboard();
        msg!(isize: pb, ffi::sel("changeCount"))
    };
    if WATCHER
        .set(Mutex::new(Watcher {
            store,
            path,
            last_change_count,
        }))
        .is_err()
    {
        // Lost a set race; the winner's timer is already scheduled.
        return;
    }
    // Safety: called once (guarded by the OnceLock above), so the class
    // name is not yet registered; the tick Imp matches its v@:@ encoding.
    unsafe { schedule_timer() };
}

/// Record that beckon itself just wrote to the general pasteboard, so the
/// watcher ignores that change (privacy rule 3). Must be called right
/// after the write, before the next poll tick can observe it. A no-op
/// before [`start`].
pub fn note_own_write() {
    let Some(w) = watcher() else {
        return;
    };
    // Safety: as in start(); changeCount returns NSInteger.
    let count = unsafe {
        let pb = general_pasteboard();
        msg!(isize: pb, ffi::sel("changeCount"))
    };
    w.lock().unwrap().last_change_count = count;
}

/// Clipboard history as registry items, for the panel. An empty query
/// returns the most recent entries (pinned first, then newest first, the
/// store's display order); a non-empty query is a case-insensitive
/// substring search via [`ClipStore::search`]. Both are capped at
/// [`MAX_ITEMS`]. Empty before [`start`].
pub fn items(query: &str) -> Vec<Item> {
    let Some(w) = watcher() else {
        return Vec::new();
    };
    let guard = w.lock().unwrap();
    items_from(&guard.store, query, now_secs())
}

/// Copy the entry behind `id` (shape "clip.<entry id>") back to the
/// general pasteboard and bump it to the top of history. Calls
/// [`note_own_write`] so the watcher does not re-record the copy. Paste
/// injection into the frontmost app is a later bead; this only writes the
/// pasteboard.
pub fn activate(id: &str) -> Result<(), String> {
    let entry_id = parse_clip_id(id).ok_or_else(|| format!("not a clipboard item id: {id}"))?;
    let w = watcher().ok_or_else(|| "clipboard watcher not started".to_string())?;
    let text = w
        .lock()
        .unwrap()
        .store
        .get(entry_id)
        .map(|e| e.text.clone())
        .ok_or_else(|| format!("unknown clipboard entry {entry_id}"))?;
    if !copy_to_pasteboard(&text) {
        return Err("pasteboard write failed".to_string());
    }
    note_own_write();
    // Re-add with the current time so the entry rises to the top; a save
    // failure loses persistence, not the copy, so it only warns.
    let mut guard = w.lock().unwrap();
    guard.store.add(&text, now_secs());
    if let Err(e) = guard.store.save(&guard.path) {
        eprintln!(
            "beckon: cannot save clipboard store {}: {e}",
            guard.path.display()
        );
    }
    Ok(())
}

/// Shape rows for the panel out of a store snapshot. Pure given `now`;
/// unit tested directly with injected clocks.
fn items_from(store: &ClipStore, query: &str, now: u64) -> Vec<Item> {
    store
        .search(query)
        .into_iter()
        .take(MAX_ITEMS)
        .map(|e| {
            Item::new(
                &format!("clip.{}", e.id),
                &title_of(&e.text),
                &subtitle_of(e, now),
                ItemKind::ClipboardEntry,
            )
        })
        .collect()
}

/// Extract the numeric entry id from an item id of shape "clip.<n>".
fn parse_clip_id(id: &str) -> Option<u64> {
    id.strip_prefix("clip.")?.parse().ok()
}

/// First line of the text, truncated to [`TITLE_MAX_CHARS`] characters
/// with a trailing ellipsis when it does not fit.
fn title_of(text: &str) -> String {
    let first = text.lines().next().unwrap_or("");
    if first.chars().count() <= TITLE_MAX_CHARS {
        first.to_string()
    } else {
        let head: String = first.chars().take(TITLE_MAX_CHARS - 1).collect();
        format!("{head}…")
    }
}

/// "pinned · 3m ago" or "3m ago": a relative age plus the pin marker.
/// Deterministic given `now`; a future `last_copied` reads as "just now".
fn subtitle_of(entry: &ClipEntry, now: u64) -> String {
    let age = age_label(now.saturating_sub(entry.last_copied));
    if entry.pinned {
        format!("pinned · {age}")
    } else {
        age
    }
}

/// Coarse relative age. Coarse on purpose: the panel refreshes per
/// keystroke, not per second, so minute granularity is plenty.
fn age_label(elapsed_secs: u64) -> String {
    if elapsed_secs < 60 {
        "just now".to_string()
    } else if elapsed_secs < 3600 {
        format!("{}m ago", elapsed_secs / 60)
    } else if elapsed_secs < 86400 {
        format!("{}h ago", elapsed_secs / 3600)
    } else {
        format!("{}d ago", elapsed_secs / 86400)
    }
}

/// Privacy rules 2 and 3 of the capture path as one pure predicate:
/// record only non-empty text within the size cap that is not marked
/// private. (The marker flag is computed at the FFI edge.)
fn should_store(text: &str, has_marker: bool) -> bool {
    !has_marker && !text.is_empty() && text.len() <= MAX_TEXT_BYTES
}

/// One poll tick: read the changeCount, and on a new value capture the
/// string contents subject to the privacy rules. The changeCount is
/// always advanced first so a skipped change is skipped forever, not
/// retried every tick.
fn poll() {
    let Some(w) = watcher() else {
        return;
    };
    // Timer callbacks between run loop pool drains can pile up the
    // autoreleased NSStrings created below; scope a pool to the tick.
    let _pool = ffi::AutoreleasePool::new();
    // Safety: changeCount returns NSInteger; types returns an NSArray or
    // nil; containsObject: takes an object and returns BOOL;
    // stringForType: takes an NSString and returns an NSString or nil
    // (both accepted by nsstring_to_string).
    let text = unsafe {
        let pb = general_pasteboard();
        let count = msg!(isize: pb, ffi::sel("changeCount"));
        {
            let mut guard = w.lock().unwrap();
            if count == guard.last_change_count {
                return;
            }
            guard.last_change_count = count;
        }
        let has_marker = has_marker_type(pb);
        let ns = msg!(Id: pb, ffi::sel("stringForType:"),
            Id: ffi::nsstring(PASTEBOARD_TYPE_STRING));
        let text = ffi::nsstring_to_string(ns);
        if !should_store(&text, has_marker) {
            return;
        }
        text
    };
    let mut guard = w.lock().unwrap();
    guard.store.add(&text, now_secs());
    if let Err(e) = guard.store.save(&guard.path) {
        eprintln!(
            "beckon: cannot save clipboard store {}: {e}",
            guard.path.display()
        );
    }
}

/// The general pasteboard singleton.
///
/// # Safety
/// Caller is on the thread discipline documented in the module header.
unsafe fn general_pasteboard() -> Id {
    msg!(Id: ffi::class("NSPasteboard"), ffi::sel("generalPasteboard"))
}

/// Privacy rule 1: does the current pasteboard change carry any of the
/// [`MARKER_TYPES`]?
///
/// # Safety
/// `pb` must be a valid NSPasteboard.
unsafe fn has_marker_type(pb: Id) -> bool {
    let types = msg!(Id: pb, ffi::sel("types"));
    if types.is_null() {
        return false;
    }
    MARKER_TYPES.iter().any(|marker| {
        msg!(Bool: types, ffi::sel("containsObject:"), Id: ffi::nsstring(marker)) != 0
    })
}

/// Write `text` to the general pasteboard as plain text. Mirrors the
/// engine's calc-copy write; callers that should not be re-captured must
/// follow up with [`note_own_write`].
fn copy_to_pasteboard(text: &str) -> bool {
    // Safety: clearContents returns NSInteger (the new change count) and
    // setString:forType: takes two NSStrings and returns BOOL.
    unsafe {
        let pb = general_pasteboard();
        let _ = msg!(isize: pb, ffi::sel("clearContents"));
        msg!(Bool: pb, ffi::sel("setString:forType:"),
            Id: ffi::nsstring(text),
            Id: ffi::nsstring(PASTEBOARD_TYPE_STRING))
            != 0
    }
}

/// The timer callback: encoding v@:@ (self, _cmd, the NSTimer).
extern "C" fn timer_tick(_this: Id, _sel: Sel, _timer: Id) {
    poll();
}

/// Define the timer-target class, instantiate it, and schedule the
/// repeating poll timer on the current thread's run loop in the common
/// modes (so it keeps ticking during panel tracking). The timer and its
/// target intentionally live for the whole process (ffi invariant 3).
///
/// # Safety
/// Call once per process (the class name must not already be registered),
/// from the thread whose run loop should host the timer.
unsafe fn schedule_timer() {
    let cls = ffi::define_class(
        "BeckonPasteboardTimerTarget",
        "NSObject",
        &[(
            "beckonPasteboardTick:",
            transmute::<extern "C" fn(Id, Sel, Id), ffi::Imp>(timer_tick),
            "v@:@",
        )],
    );
    let target = msg!(Id: msg!(Id: cls, ffi::sel("alloc")), ffi::sel("init"));
    // timerWithTimeInterval:target:selector:userInfo:repeats: returns an
    // autoreleased timer; scheduling it retains it, and the timer retains
    // its target, so both survive the pool.
    let timer = msg!(Id: ffi::class("NSTimer"),
        ffi::sel("timerWithTimeInterval:target:selector:userInfo:repeats:"),
        f64: POLL_INTERVAL_SECONDS,
        Id: target,
        Sel: ffi::sel("beckonPasteboardTick:"),
        Id: ffi::NIL,
        Bool: ffi::YES);
    let run_loop = msg!(Id: ffi::class("NSRunLoop"), ffi::sel("currentRunLoop"));
    // The literal value of NSRunLoopCommonModes.
    msg!((): run_loop, ffi::sel("addTimer:forMode:"),
        Id: timer, Id: ffi::nsstring("kCFRunLoopCommonModes"));
}

