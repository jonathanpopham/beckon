    /// Read the general pasteboard's plain-text contents ("" for none).
    ///
    /// # Safety
    /// Hardware-test only; see the module threading notes.
    unsafe fn read_pasteboard_string() -> String {
        let pb = general_pasteboard();
        let ns = msg!(Id: pb, ffi::sel("stringForType:"),
            Id: ffi::nsstring(PASTEBOARD_TYPE_STRING));
        ffi::nsstring_to_string(ns)
    }

    /// Run the current thread's run loop for `seconds`, firing any timers
    /// scheduled on it (the default mode is one of the common modes).
    ///
    /// # Safety
    /// runUntilDate: takes an NSDate; dateWithTimeIntervalSinceNow:
    /// returns an autoreleased NSDate.
    unsafe fn pump_run_loop(seconds: f64) {
        let run_loop = msg!(Id: ffi::class("NSRunLoop"), ffi::sel("currentRunLoop"));
        let date = msg!(Id: ffi::class("NSDate"),
            ffi::sel("dateWithTimeIntervalSinceNow:"), f64: seconds);
        msg!((): run_loop, ffi::sel("runUntilDate:"), Id: date);
    }

    /// What the hardware body observed, asserted only after the owner's
    /// pasteboard has been restored.
    struct HardwareObservations {
        wrote: bool,
        captured_title: Option<String>,
        copy_count_after_capture: Option<u64>,
        store_file_mode: u32,
        activate_result: Result<(), String>,
        pasteboard_after_activate: String,
        copy_count_after_second_pump: Option<u64>,
    }

    fn copy_count_of(unique: &str) -> Option<u64> {
        let guard = watcher().expect("watcher started").lock().unwrap();
        guard.store.search(unique).first().map(|e| e.copy_count)
    }

    fn hardware_body(unique: &str) -> HardwareObservations {
        start();
        // A second start must be a no-op, not a double class definition.
        start();
        // Write like a foreign app would: no note_own_write, so the
        // watcher must capture it on the next tick.
        let wrote = copy_to_pasteboard(unique);
        // Safety: helpers above; the poll interval is 0.5 s, so 1.2 s of
        // pumping guarantees at least one tick.
        unsafe { pump_run_loop(1.2) };
        let captured_title = items("")
            .first()
            .map(|i| i.title.clone())
            .filter(|t| t == &title_of(unique));
        let copy_count_after_capture = copy_count_of(unique);
        let store_file_mode = {
            use std::os::unix::fs::PermissionsExt as _;
            let guard = watcher().expect("watcher started").lock().unwrap();
            std::fs::metadata(&guard.path)
                .map(|m| m.permissions().mode() & 0o777)
                .unwrap_or(0)
        };
        let entry_id = {
            let guard = watcher().expect("watcher started").lock().unwrap();
            guard.store.search(unique).first().map(|e| e.id)
        };
        let activate_result = match entry_id {
            Some(id) => activate(&format!("clip.{id}")),
            None => Err("entry never captured".to_string()),
        };
        // Safety: as above.
        let pasteboard_after_activate = unsafe { read_pasteboard_string() };
        // The activate write bumped the changeCount; note_own_write must
        // keep the watcher from re-recording it as a fresh copy.
        unsafe { pump_run_loop(1.2) };
        let copy_count_after_second_pump = copy_count_of(unique);
        HardwareObservations {
            wrote,
            captured_title,
            copy_count_after_capture,
            store_file_mode,
            activate_result,
            pasteboard_after_activate,
            copy_count_after_second_pump,
        }
    }

    /// The live-pasteboard round trip. Ignored by default because it
    /// touches the machine's real clipboard; run it alone with
    /// `cargo test -p beckon-macos -- --ignored hardware`. The owner's
    /// clipboard string is saved first and restored exactly, asserted
    /// last thing, and the store lands under a temp BECKON_HOME that is
    /// deleted afterward.
    #[test]
    #[ignore = "drives the live pasteboard; run manually with --ignored"]
    fn hardware_pasteboard_round_trip() {
        let _pool = ffi::AutoreleasePool::new();
        let dir = std::env::temp_dir().join(format!("beckon-pasteboard-hw-{}", std::process::id()));
        std::fs::create_dir_all(&dir).expect("create temp BECKON_HOME");
        std::env::set_var("BECKON_HOME", &dir);
        // Safety: read helper above.
        let original = unsafe { read_pasteboard_string() };
        let unique = format!(
            "beckon pasteboard hardware test {} {}",
            std::process::id(),
            now_secs()
        );
        // Everything that mutates the pasteboard runs under catch_unwind
        // so the restore below happens even if an assertion-shaped step
        // panics mid-flight.
        let outcome =
            std::panic::catch_unwind(std::panic::AssertUnwindSafe(|| hardware_body(&unique)));
        // Restore the owner's clipboard exactly, no matter what happened.
        if original.is_empty() {
            // Safety: clearContents returns NSInteger.
            unsafe {
                let pb = general_pasteboard();
                let _ = msg!(isize: pb, ffi::sel("clearContents"));
            }
        } else {
            assert!(copy_to_pasteboard(&original), "restore write failed");
        }
        note_own_write();
        // Safety: read helper above.
        let restored = unsafe { read_pasteboard_string() };
        std::env::remove_var("BECKON_HOME");
        let _ = std::fs::remove_dir_all(&dir);
        // Proof of restoration by length and content hash; the owner's
        // clipboard text itself is never printed.
        println!(
            "hardware: original len={} fnv={:016x}; restored len={} fnv={:016x}",
            original.len(),
            beckon_core::clipstore::fnv1a64(original.as_bytes()),
            restored.len(),
            beckon_core::clipstore::fnv1a64(restored.as_bytes()),
        );
        assert_eq!(
            restored, original,
            "the owner's clipboard must be exactly as it was found"
        );
        let obs = match outcome {
            Ok(obs) => obs,
            Err(panic) => std::panic::resume_unwind(panic),
        };
        assert!(obs.wrote, "test write to the pasteboard failed");
        assert_eq!(
            obs.captured_title,
            Some(title_of(&unique)),
            "watcher must capture the test string within one tick"
        );
        assert_eq!(obs.copy_count_after_capture, Some(1));
        assert_eq!(obs.store_file_mode, 0o600, "clipboard.json must be 0600");
        assert_eq!(obs.activate_result, Ok(()));
        assert_eq!(
            obs.pasteboard_after_activate, unique,
            "activate must copy the full text back"
        );
        // 1 from capture + 1 from the activate bump; note_own_write kept
        // the watcher from adding a third.
        assert_eq!(obs.copy_count_after_second_pump, Some(2));
    }
